    private fun isWide(cp: Int): Boolean = when (cp) {
        in 0x1100..0x115F,     // Hangul Jamo
        in 0x2329..0x232A,     // Angle brackets
        in 0x2E80..0x303E,     // CJK Radicals, Kangxi, CJK Symbols
        in 0x3041..0x33BF,     // Hiragana, Katakana, Bopomofo, Hangul Compat, Kanbun, CJK Compat
        in 0x33D0..0x33FF,     // CJK Compatibility
        in 0x3400..0x4DBF,     // CJK Unified Ideographs Extension A
        in 0x4E00..0x9FFF,     // CJK Unified Ideographs
        in 0xA000..0xA4CF,     // Yi Syllables / Radicals
        in 0xAC00..0xD7AF,     // Hangul Syllables
        in 0xF900..0xFAFF,     // CJK Compatibility Ideographs
        in 0xFE10..0xFE19,     // Vertical forms
        in 0xFE30..0xFE6F,     // CJK Compatibility Forms + Small Forms
        in 0xFF01..0xFF60,     // Fullwidth ASCII
        in 0xFFE0..0xFFE6,     // Fullwidth sign
        in 0x1F000..0x1F9FF,   // Mahjong, Dominos, Playing Cards, Emoji
        in 0x1FA00..0x1FA6F,   // Chess symbols, Extended-A
        in 0x1FA70..0x1FAFF,   // Extended-A continued
        in 0x20000..0x2FFFF,   // CJK Extension B-F, Compat Ideographs Supp
        in 0x30000..0x3FFFF,   // CJK Extension G-I
        -> true
        else -> false
    }
