object CharWidth {

    /** Returns the display width of [cp] in terminal columns (0, 1, or 2). */
    fun of(cp: Int): Int = when {
        // C0/C1 controls, DEL.
        cp < 0x20 || cp == 0x7F || cp in 0x80..0x9F -> 0

        // Combining characters (zero-width).
        isCombining(cp) -> 0

        // East Asian Fullwidth / Wide.
        isWide(cp) -> 2

        else -> 1
    }

